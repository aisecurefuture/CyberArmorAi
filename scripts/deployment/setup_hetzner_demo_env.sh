#!/usr/bin/env bash
set -euo pipefail

# Create a root-owned production env file for the CyberArmor demo stack.
#
# Run as root on the target Ubuntu server:
#   sudo APP_DOMAIN=app.cyberarmor.ai \
#        ADMIN_DOMAIN=admin.cyberarmor.ai \
#        MARKETING_DOMAIN=cyberarmor.ai \
#        WWW_MARKETING_DOMAIN=www.cyberarmor.ai \
#        DOCS_DOMAIN=docs.cyberarmor.ai \
#        SUPPORT_DOMAIN=support.cyberarmor.ai \
#        LE_EMAIL=ops@cyberarmor.ai \
#        ADMIN_DASHBOARD_ALLOWED_EMAILS=admin@cyberarmor.ai \
#        bash scripts/deployment/setup_hetzner_demo_env.sh

if [[ "${EUID}" -ne 0 ]]; then
  echo "Run this script as root."
  exit 1
fi

TARGET_ENV_FILE="${TARGET_ENV_FILE:-/etc/cyberarmor/demo.env}"
TARGET_DIR="$(dirname "${TARGET_ENV_FILE}")"

APP_DOMAIN="${APP_DOMAIN:-app.cyberarmor.ai}"
ADMIN_DOMAIN="${ADMIN_DOMAIN:-admin.cyberarmor.ai}"
MARKETING_DOMAIN="${MARKETING_DOMAIN:-cyberarmor.ai}"
WWW_MARKETING_DOMAIN="${WWW_MARKETING_DOMAIN:-www.cyberarmor.ai}"
DOCS_DOMAIN="${DOCS_DOMAIN:-docs.cyberarmor.ai}"
SUPPORT_DOMAIN="${SUPPORT_DOMAIN:-support.cyberarmor.ai}"
LE_EMAIL="${LE_EMAIL:-ops@cyberarmor.ai}"
ADMIN_DASHBOARD_ALLOWED_EMAILS="${ADMIN_DASHBOARD_ALLOWED_EMAILS:-admin@cyberarmor.ai}"

POSTGRES_USER="${POSTGRES_USER:-cyberarmor}"
POSTGRES_DB="${POSTGRES_DB:-cyberarmor}"

rand_secret() {
  openssl rand -base64 48 | tr -d '\n'
}

if [[ -f "${TARGET_ENV_FILE}" ]]; then
  echo "Refusing to overwrite existing env file: ${TARGET_ENV_FILE}" >&2
  echo "Edit it in place, or remove it intentionally before rerunning this script." >&2
  exit 1
fi

install -d -m 700 "${TARGET_DIR}"

POSTGRES_PASSWORD="$(rand_secret)"
CYBERARMOR_API_SECRET="$(rand_secret)"
CYBERARMOR_JWT_SECRET="$(rand_secret)"
POLICY_API_SECRET="$(rand_secret)"
DETECTION_API_SECRET="$(rand_secret)"
IDENTITY_API_SECRET="$(rand_secret)"
SIEM_API_SECRET="$(rand_secret)"
COMPLIANCE_API_SECRET="$(rand_secret)"
PROXY_AGENT_API_SECRET="$(rand_secret)"
RESPONSE_API_SECRET="$(rand_secret)"
AGENT_IDENTITY_API_SECRET="$(rand_secret)"
AGENT_IDENTITY_JWT_SECRET="$(rand_secret)"
ROUTER_API_SECRET="$(rand_secret)"
ROUTER_ENCRYPTION_KEY="$(openssl rand -hex 32)"
AUDIT_API_SECRET="$(rand_secret)"
TELEMETRY_API_KEY="$(rand_secret)"
INTEGRATION_CONTROL_API_SECRET="$(rand_secret)"
SECRETS_SERVICE_API_SECRET="$(rand_secret)"
OPENBAO_DEV_ROOT_TOKEN="$(rand_secret)"
ADMIN_DASHBOARD_SESSION_SECRET="$(rand_secret)"
CUSTOMER_PORTAL_SESSION_SECRET="$(rand_secret)"

cat > "${TARGET_ENV_FILE}" <<EOF
# Generated by scripts/deployment/setup_hetzner_demo_env.sh
# Review and edit before first deployment.

# Public DNS / URLs
LE_EMAIL=${LE_EMAIL}
APP_DOMAIN=${APP_DOMAIN}
ADMIN_DOMAIN=${ADMIN_DOMAIN}
MARKETING_DOMAIN=${MARKETING_DOMAIN}
WWW_MARKETING_DOMAIN=${WWW_MARKETING_DOMAIN}
DOCS_DOMAIN=${DOCS_DOMAIN}
SUPPORT_DOMAIN=${SUPPORT_DOMAIN}
CUSTOMER_PORTAL_PUBLIC_URL=https://${APP_DOMAIN}

# Core auth secrets
CYBERARMOR_API_SECRET=${CYBERARMOR_API_SECRET}
CYBERARMOR_JWT_SECRET=${CYBERARMOR_JWT_SECRET}
POLICY_API_SECRET=${POLICY_API_SECRET}
POLICY_API_KEY=${POLICY_API_SECRET}
DETECTION_API_SECRET=${DETECTION_API_SECRET}
IDENTITY_API_SECRET=${IDENTITY_API_SECRET}
SIEM_API_SECRET=${SIEM_API_SECRET}
COMPLIANCE_API_SECRET=${COMPLIANCE_API_SECRET}
PROXY_AGENT_API_SECRET=${PROXY_AGENT_API_SECRET}
RESPONSE_API_SECRET=${RESPONSE_API_SECRET}
AGENT_IDENTITY_API_SECRET=${AGENT_IDENTITY_API_SECRET}
AGENT_IDENTITY_JWT_SECRET=${AGENT_IDENTITY_JWT_SECRET}
ROUTER_API_SECRET=${ROUTER_API_SECRET}
ROUTER_ENCRYPTION_KEY=${ROUTER_ENCRYPTION_KEY}
AUDIT_API_SECRET=${AUDIT_API_SECRET}
TELEMETRY_API_KEY=${TELEMETRY_API_KEY}
INTEGRATION_CONTROL_API_SECRET=${INTEGRATION_CONTROL_API_SECRET}
SECRETS_SERVICE_API_SECRET=${SECRETS_SERVICE_API_SECRET}
CYBERARMOR_ENV_FILE=${TARGET_ENV_FILE}
HOST_BIND=127.0.0.1
CADDY_DNS_1=1.1.1.1
CADDY_DNS_2=8.8.8.8
OPENBAO_DEV_ROOT_TOKEN=${OPENBAO_DEV_ROOT_TOKEN}

# Dashboard / customer auth
ADMIN_DASHBOARD_ALLOWED_EMAILS=${ADMIN_DASHBOARD_ALLOWED_EMAILS}
ADMIN_DASHBOARD_SESSION_SECRET=${ADMIN_DASHBOARD_SESSION_SECRET}
ADMIN_DASHBOARD_AUTH_DEV_CODE_ECHO=false
ADMIN_DASHBOARD_CODE_TTL_SECONDS=600
ADMIN_DASHBOARD_SESSION_TTL_SECONDS=28800
ADMIN_DASHBOARD_MAX_CODE_ATTEMPTS=5
ADMIN_DASHBOARD_COOKIE_SECURE=true
ADMIN_DASHBOARD_SMTP_HOST=
ADMIN_DASHBOARD_SMTP_PORT=587
ADMIN_DASHBOARD_SMTP_USER=
ADMIN_DASHBOARD_SMTP_PASSWORD=
ADMIN_DASHBOARD_SMTP_FROM=no-reply@${MARKETING_DOMAIN}
ADMIN_DASHBOARD_SMTP_TLS=true

CUSTOMER_PORTAL_SESSION_SECRET=${CUSTOMER_PORTAL_SESSION_SECRET}
CUSTOMER_PORTAL_AUTH_DEV_CODE_ECHO=false
CUSTOMER_PORTAL_CODE_TTL_SECONDS=600
CUSTOMER_PORTAL_SESSION_TTL_SECONDS=28800
CUSTOMER_PORTAL_MAX_CODE_ATTEMPTS=5
CUSTOMER_PORTAL_COOKIE_SECURE=true
CUSTOMER_PORTAL_SMTP_HOST=
CUSTOMER_PORTAL_SMTP_PORT=587
CUSTOMER_PORTAL_SMTP_USER=
CUSTOMER_PORTAL_SMTP_PASSWORD=
CUSTOMER_PORTAL_SMTP_FROM=no-reply@${MARKETING_DOMAIN}
CUSTOMER_PORTAL_SMTP_TLS=true

MARKETING_CONTACT_SMTP_HOST=
MARKETING_CONTACT_SMTP_PORT=587
MARKETING_CONTACT_SMTP_USER=
MARKETING_CONTACT_SMTP_PASSWORD=
MARKETING_CONTACT_SMTP_FROM=no-reply@${MARKETING_DOMAIN}
MARKETING_CONTACT_SMTP_TLS=true
MARKETING_CONTACT_TO=hello@${MARKETING_DOMAIN}

# Database
POSTGRES_USER=${POSTGRES_USER}
POSTGRES_PASSWORD=${POSTGRES_PASSWORD}
POSTGRES_DB=${POSTGRES_DB}
DATABASE_URL=postgresql://${POSTGRES_USER}:${POSTGRES_PASSWORD}@postgres:5432/${POSTGRES_DB}

# Host port bindings
POSTGRES_HOST_PORT=5432
REDIS_HOST_PORT=6379
OPA_HOST_PORT=8181
OPENBAO_HOST_PORT=8200
POLICY_HOST_PORT=8001
AGENT_IDENTITY_HOST_PORT=8008
AI_ROUTER_HOST_PORT=8009
AUDIT_HOST_PORT=8011
SECRETS_SERVICE_HOST_PORT=8013
CUSTOMER_PORTAL_HOST_PORT=3001
MARKETING_HOST_PORT=3002
DOCS_HOST_PORT=3004

# Secure runtime defaults
CYBERARMOR_ENFORCE_SECURE_SECRETS=true
CYBERARMOR_ALLOW_INSECURE_DEFAULTS=false
CYBERARMOR_ENFORCE_MTLS=false
CYBERARMOR_TLS_CA_FILE=/etc/cyberarmor/tls/ca.crt
CYBERARMOR_TLS_CERT_FILE=/etc/cyberarmor/tls/tls.crt
CYBERARMOR_TLS_KEY_FILE=/etc/cyberarmor/tls/tls.key
CYBERARMOR_ENABLE_NATIVE_TLS_LISTENER=false
CYBERARMOR_REQUIRE_CLIENT_CERT=false

# PQC rollout defaults
CYBERARMOR_PQC_BACKEND=secrets-service
CYBERARMOR_PQC_AUTH_ENABLED=false
CYBERARMOR_PQC_OUTBOUND_STRICT=false
CYBERARMOR_PQC_ALLOW_PLAINTEXT=true
CYBERARMOR_PQC_REQUIRE_ENCRYPTED=false
CYBERARMOR_PQC_PUBLIC_KEY_CACHE_TTL_SECONDS=300
CYBERARMOR_PQC_PUBLIC_KEY_TIMEOUT_SECONDS=3
CYBERARMOR_PQC_ROTATION_INTERVAL_SECONDS=86400

# Service URLs / secrets backend
CONTROL_PLANE_URL=http://control-plane:8000
POLICY_SERVICE_URL=http://policy:8001
DETECTION_SERVICE_URL=http://detection:8002
AISR_RUNTIME_URL=http://runtime:8000
PROXY_AGENT_URL=http://proxy-agent:8010
AGENT_IDENTITY_URL=http://agent-identity:8008
AI_ROUTER_URL=http://ai-router:8009
AUDIT_URL=http://audit:8011
TELEMETRY_ENDPOINT=http://siem-connector:8005/ingest
COMPLIANCE_URL=http://compliance:8006
INTEGRATION_CONTROL_URL=http://integration-control:8012
SECRETS_SERVICE_URL=http://secrets-service:8013
OPENBAO_ADDR=http://openbao:8200
OPENBAO_NAMESPACE=
OPENBAO_KV_MOUNT=cyberarmor-kv
OPENBAO_TRANSIT_MOUNT=cyberarmor-transit
OPENBAO_TIMEOUT_SECONDS=5
ROUTER_USE_SECRETS_SERVICE=true
ROUTER_REQUIRE_SECRETS_SERVICE=false

# Tenant/runtime defaults
TENANT_ID=default
DEFAULT_PROXY_RUNTIME_MODE=mitm
TENANT_PROXY_MODES={}
TENANT_MODE_CACHE_TTL_SECONDS=60

# Proxy behavior
PROXY_MODE=transparent
FAIL_OPEN=true
LOG_LEVEL=INFO
EOF

chown root:root "${TARGET_ENV_FILE}"
chmod 600 "${TARGET_ENV_FILE}"

echo "Created ${TARGET_ENV_FILE}"
echo
echo "Next steps:"
echo "- review SMTP values and allowed admin emails"
echo "- if enabling OIDC/SSO later, configure it in the app after first boot"
echo "- deploy with DEMO_ENV_FILE=${TARGET_ENV_FILE}"
echo "- keep this file out of git and out of shell history"
